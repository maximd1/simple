package com.algorithms.simple.seddlepoints;

public class SeddlePoint {
	int i;
	int j;
	int value;

	public SeddlePoint(int i, int j, int value) {
		super();
		this.i = i;
		this.j = j;
		this.value = value;
	}

	public int getI() {
		return i;
	}
	public void setI(int i) {
		this.i = i;
	}
	public int getJ() {
		return j;
	}
	public void setJ(int j) {
		this.j = j;
	}
	public int getValue() {
		return value;
	}
	public void setValue(int value) {
		this.value = value;
	}
}